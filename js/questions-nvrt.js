  function shapeMarkup(token, cell = 18, ox = 16, oy = 16) {
    const size = token.size || Math.max(7, Math.round(cell * 0.42));
    const cx = ox + token.col * cell;
    const cy = oy + token.row * cell;
    const stroke = token.stroke || ink;
    const fill = token.hollow ? "#ffffff" : (token.fill || ink);
    const rotate = token.rotate || 0;

    let inner = "";
    if (token.kind === "circle") {
      inner = `<circle cx="${cx}" cy="${cy}" r="${size}" fill="${fill}" stroke="${stroke}" stroke-width="2"/>`;
    } else if (token.kind === "square") {
      inner = `<rect x="${cx - size}" y="${cy - size}" width="${size * 2}" height="${size * 2}" rx="3" fill="${fill}" stroke="${stroke}" stroke-width="2"/>`;
    } else if (token.kind === "diamond") {
      inner = `<polygon points="${cx},${cy - size} ${cx + size},${cy} ${cx},${cy + size} ${cx - size},${cy}" fill="${fill}" stroke="${stroke}" stroke-width="2"/>`;
    } else {
      inner = `<polygon points="${cx},${cy - size} ${cx + size},${cy + size} ${cx - size},${cy + size}" fill="${fill}" stroke="${stroke}" stroke-width="2"/>`;
    }

    return rotate ? `<g transform="rotate(${rotate} ${cx} ${cy})">${inner}</g>` : inner;
  }

